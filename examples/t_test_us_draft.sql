-- Inference statistics for the 1969 draft (first-half vs second-half year)
SELECT CAST(STATS_TTEST(
    CAST(n69 AS DOUBLE),
    CASE
        WHEN month BETWEEN 1 AND 6  THEN 1
        WHEN month BETWEEN 7 AND 12 THEN 2
    END,
    0.05
) AS CHAR(1000)) AS ttest_1969
FROM us_draft
WHERE month BETWEEN 1 AND 12;

-- Per-group descriptives for the 1969 draft
SELECT CAST(STATS_TTEST_GROUPS(
    CAST(n69 AS DOUBLE),
    CASE
        WHEN month BETWEEN 1 AND 6  THEN 1
        WHEN month BETWEEN 7 AND 12 THEN 2
    END
) AS CHAR(1000)) AS ttest_groups_1969
FROM us_draft
WHERE month BETWEEN 1 AND 12;


-- Compare 1969 and 1970 drafts: compute STATS_TTEST once per draft year,
-- then extract individual fields with JSON_EXTRACT.
WITH half_year_drafts AS (
    -- 1969 draft numbers categorised by half-year
    SELECT
        '1969 Draft'           AS draft_year,
        CAST(n69 AS DOUBLE)    AS draft_num,
        IF(month BETWEEN 1 AND 6, 1.0, 2.0) AS grp
    FROM us_draft
    WHERE n69 IS NOT NULL AND month BETWEEN 1 AND 12

    UNION ALL

    -- 1970 draft numbers categorised by half-year
    SELECT
        '1970 Draft'           AS draft_year,
        CAST(n70 AS DOUBLE)    AS draft_num,
        IF(month BETWEEN 1 AND 6, 1.0, 2.0) AS grp
    FROM us_draft
    WHERE n70 IS NOT NULL AND month BETWEEN 1 AND 12
),
ttest_results AS (
    SELECT
        draft_year,
        CAST(STATS_TTEST(draft_num, grp, 0.05) AS CHAR(1000))  AS ttest_json,
        CAST(STATS_TTEST_GROUPS(draft_num, grp) AS CHAR(1000))  AS groups_json
    FROM half_year_drafts
    GROUP BY draft_year
)
SELECT
    draft_year,
    JSON_EXTRACT(groups_json, '$.mean_1')         AS mean_jan_jun,
    JSON_EXTRACT(groups_json, '$.mean_2')         AS mean_jul_dec,
    JSON_EXTRACT(groups_json, '$.variance_1')     AS variance_jan_jun,
    JSON_EXTRACT(groups_json, '$.variance_2')     AS variance_jul_dec,
    JSON_EXTRACT(groups_json, '$.n_1')            AS observations_jan_jun,
    JSON_EXTRACT(groups_json, '$.n_2')            AS observations_jul_dec,
    JSON_EXTRACT(ttest_json,  '$.pooled_var')     AS pooled_variance,
    JSON_EXTRACT(ttest_json,  '$.df')             AS degrees_of_freedom,
    JSON_EXTRACT(ttest_json,  '$.t')              AS t_statistic,
    JSON_EXTRACT(ttest_json,  '$.p_one_tail')     AS p_value_one_tail,
    JSON_EXTRACT(ttest_json,  '$.t_crit_one_tail') AS t_critical_one_tail,
    JSON_EXTRACT(ttest_json,  '$.p_two_tail')     AS p_value_two_tail,
    JSON_EXTRACT(ttest_json,  '$.t_crit_two_tail') AS t_critical_two_tail,
    IF(JSON_EXTRACT(ttest_json, '$.p_two_tail') < 0.05,
       'Significant Difference',
       'No Significant Difference')               AS hypothesis_result
FROM ttest_results
ORDER BY draft_year\G

